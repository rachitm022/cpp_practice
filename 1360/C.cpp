void solve(){
	int n;
	cin>>n;
	int cnt[101] = {0};
	int even=0,odd=0,pairs=0;
	for(int i=0;i<n;i++){
		int t;
		cin>>t;
		cnt[t]++;
		if(t%2) odd++;
		else even++;
	}
	for(int i=1;i<=100;i++){
		if(cnt[i]&&cnt[i-1]) pairs++;
	}
	if(even%2==odd%2){
		if(even%2){
			if(pairs) cout<<"YES"<<endl;
			else cout<<"NO"<<endl;
			return;
		}
		else{
			cout<<"YES"<<endl;
			return;
		}
	}
	cout<<"NO"<<endl;

}
int main(){
	ios::sync_with_stdio(0);
	cin.tie(NULL);
	cout.tie(NULL);
	int T;
	cin>>T;
	while(T--)
		solve();

	return 0;
}

ll n,d,m,l;
ll ans;
ll search(ll lo,ll hi){
	if(lo<=hi){
		// cout<<lo<<" "<<hi<<" "<<ans<<endl;
		ll mi = (lo+hi)/2;
		if((d*mi)%m>l){
			ans = min(ans,d*mi);
			ans = min(ans,search(lo,mi-1));
		}
		else{
			ans = min({ans,search(lo,mi-1),search(mi+1,hi)});
		}
		return ans;
	}
	else return 1e12;
}
void solve(){
	cin>>n>>d>>m>>l;
	ans = ((n-1)*m+l) - ((n-1)*m+l)%d + d;
	ll hi = m;
	cout<<search(0,hi)<<endl;
}

const int MAXN = 1e6+1;
int a[4*MAXN],b[4*MAXN],c[4*MAXN];
int n;
string s;
void build(int v,int tl,int tr){
	if(tl==tr){
		a[v] = 0;
		b[v] = (s[tl]=='(');
		c[v] = (s[tl]==')');

	}
	else{
		int tm = (tl+tr)/2;
		build(2*v,tl,tm);
		build(2*v+1,tm+1,tr);
		int x = min(b[2*v],c[2*v+1]);
		a[v] = a[2*v] + a[2*v+1] + x;
		b[v] = b[2*v] + b[2*v+1] - x;
		c[v] = c[2*v] + c[2*v+1] - x;
		// printf("%d %d %d %d %d\n",tl,tr,a[v],b[v],c[v]);
	}
}

vector<int> queryA(int l,int r,int v=1,int tl=0,int tr=n-1){
	if(tl>r||tr<l){
		return {0,0,0};
	}
	if(tl>=l&&tr<=r){
		return {a[v],b[v],c[v]};
	}

	int tm = (tl+tr)/2;
	auto x1 = queryA(l,r,2*v,tl,tm);
	auto x2 = queryA(l,r,2*v+1,tm+1,tr);
	int x = min(x1[1],x2[2]);
	return {x1[0]+x2[0]+x,x1[1]+x2[1]-x,x1[2]+x2[2]-x};
} 

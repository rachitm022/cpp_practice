const int MAXN = (1<<19+1);
int t[MAXN];
int tot = 0;
void build(int a[],int level,int v,int tl, int tr){
	tot = max(tot,level);
	if(tl==tr){
		t[v] = a[tl];
	}
	else{
		int tm = (tl+tr)/2;
		build(a,level+1,2*v,tl,tm);
		build(a,level+1,2*v + 1,tm+1,tr);
		if((tot-level)%2) t[v] = t[2*v]|t[2*v+1];
		else t[v] = t[2*v]^t[2*v+1];
	}
}

void update(int v,int level,int tl,int tr,int pos,int val){
	if(tl==tr){
		t[v] = val;
	}
	else{
		int tm = (tl+tr)/2;
		if(pos<=tm){
			update(2*v,level+1,tl,tm,pos,val);
		}
		else{
			update(2*v+1,level+1,tm+1,tr,pos,val);
		}
		if((tot-level)%2) t[v] = t[2*v]|t[2*v+1];
		else t[v] = t[2*v]^t[2*v+1];
	}
}

int sum(int v,int level,int tl,int tr,int l,int r){
	if(l>r) return 0;

	if(tl==l&&tr==r){
		return t[v];
	}

	int tm = (tl+tr)/2;
	if((tot-level)%2) return sum(2*v,level+1,tl,tm,l,min(tm,r))|sum(2*v+1,level+1,tm+1,tr,max(l,tm+1),r);
	else return sum(2*v,level+1,tl,tm,l,min(tm,r))^sum(2*v+1,level+1,tm+1,tr,max(l,tm+1),r);
}
